#! /usr/bin/ruby

def Add(num1, num2)
  return num1+num2
end

def Sub(num1, num2)
  return num1-num2
end

def Mult(num1, num2)
  return num1*num2
end

def Div(num1, num2)
  return num1/num2
end

number1 = 112.0
number2 = 41.0

puts Add(number1, number2)
puts Sub(number1, number2)
puts Mult(number1, number2)
puts Div(number1, number2)
